{
    c.save();
    c.translate(xk,yk);
    if(no==0) Floppy(canv);
    if(no==1) Brush(canv);
    if(no==2) Paint(canv);
    if(no==3) Elevation(canv);
    if(no==4) Undo(canv);
    if(no==5) Redo(canv);
    c.restore();
}

